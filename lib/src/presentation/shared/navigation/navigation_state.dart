/// Enum для управления навигацией из ViewModels.
///
/// Используется ViewModels для указания UI слою, куда необходимо
/// выполнить навигацию после успешного выполнения операции.
enum NavigationAction {
  /// Навигация не требуется
  none,

  /// Переход на экран счетчика (главный экран)
  navigateToCounter,

  /// Переход на экран входа
  navigateToLogin,

  /// Переход на экран регистрации
  navigateToSignup,
}
